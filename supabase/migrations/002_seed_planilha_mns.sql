CREATE TABLE IF NOT EXISTS impressoras (
  id                    uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  fabricante            text NOT NULL,
  modelo                text NOT NULL,
  codigo                text NOT NULL UNIQUE,
  nivel_uso             text,
  potencia_w            numeric(10,2),
  concessionaria        text,
  custo_energia_hora    numeric(10,4),
  taxa_ocupacao         numeric(5,4),
  valor_equipamento     numeric(15,2),
  lucro_estimado_hora   numeric(10,4),
  taxa_erros            numeric(5,4),
  horas_dia             int DEFAULT 12,
  dias_mes              int DEFAULT 15,
  horas_mes             int DEFAULT 180,
  custo_depreciacao_hora numeric(10,6),
  ativo                 boolean NOT NULL DEFAULT true,
  created_at            timestamptz NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS filamentos (
  id                uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  fabricante        text NOT NULL,
  modelo            text NOT NULL,
  codigo            text NOT NULL UNIQUE,
  tipo              text NOT NULL,
  peso_kg           numeric(6,3),
  comprimento_m     int,
  taxa_falha        numeric(5,4),
  custo_carretel    numeric(10,2),
  custo_por_grama   numeric(10,4),
  ativo             boolean NOT NULL DEFAULT true,
  created_at        timestamptz NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS manutencoes (
  id             uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  item           text NOT NULL,
  vida_util_h    int,
  preco          numeric(10,2),
  custo_por_hora numeric(10,6),
  created_at     timestamptz NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS marketplaces (
  id             uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  nome           text NOT NULL UNIQUE,
  tarifa_fixa    numeric(10,2) DEFAULT 0,
  comissao_pct   numeric(5,4) DEFAULT 0,
  ativo          boolean NOT NULL DEFAULT true,
  created_at     timestamptz NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS descontos_quantidade (
  id             uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  faixa          text NOT NULL,
  qtde_de        int NOT NULL,
  qtde_ate       int NOT NULL,
  desconto_pct   numeric(5,4) NOT NULL,
  ativo          boolean NOT NULL DEFAULT true,
  created_at     timestamptz NOT NULL DEFAULT now()
);

